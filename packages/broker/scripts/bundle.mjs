import { mkdir, readFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const brokerRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(brokerRoot, "../..");
const brokerPackage = JSON.parse(await readFile(path.join(brokerRoot, "package.json"), "utf8"));
const outfile = path.join(
  repoRoot,
  "apps",
  "desktop",
  "src-tauri",
  "resources",
  "broker",
  "server.mjs"
);

await mkdir(path.dirname(outfile), { recursive: true });

await build({
  entryPoints: [path.join(brokerRoot, "src", "server.ts")],
  outfile,
  bundle: true,
  platform: "node",
  target: "node22",
  format: "esm",
  sourcemap: false,
  minify: false,
  banner: {
    js: [
      "// Bundled Codex Spark Broker. Generated by packages/broker/scripts/bundle.mjs",
      'import { createRequire } from "node:module";',
      "const require = createRequire(import.meta.url);",
    ].join("\n"),
  },
  define: {
    __BROKER_VERSION__: JSON.stringify(brokerPackage.version),
  },
});

console.log(`Bundled broker -> ${outfile}`);
